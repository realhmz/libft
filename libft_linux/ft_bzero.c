#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*a;
	size_t			i;

	a = s;
	i = 0;
	if (!s)
		return ;
	while (i < n)
	{
		a[i] = 0;
		i++;
	}
}
